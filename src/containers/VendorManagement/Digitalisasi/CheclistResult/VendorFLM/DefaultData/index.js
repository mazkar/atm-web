export const changeStateTop=async(old, keyname, newValues)=>{
  const newArr = [];
  //   console.log(">>> old", old[keyname]); 
  old[keyname].map((item, index)=>{
    // console.log(">>> item", item);
    if(newValues[index]){
      newArr.push({
        ...item,
        value: newValues[index]
      });
    }else{
      newArr.push(item);
    }
  });
  return newArr;
};